String translate(String key) {
  final languageKey = selectedLanguageNotifier.value["key"];
  switch (languageKey) {
    case "en":
      return MultiLangStrings.englishStrings[key] ?? "$key";
    case "mg":
      return MultiLangStrings.malagasyStrings[key] ?? "$key";
    case "it":
      return MultiLangStrings.italianStrings[key] ?? "$key";
    case "pl":
      return MultiLangStrings.polishStrings[key] ?? "$key";
    case "fr":
    default:
      return MultiLangStrings.frenchStrings[key] ?? "$key";
  }
}

